    private void btnRetrosederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetrosederActionPerformed
        String indice = txtIndice.getText();
        int num = Integer.parseInt(indice);
        if (num > 0) {
            num = num - 1;
            indice = String.valueOf(num);
            txtIndice.setText(indice);
            
            txtDni.setText(dnis[num]);
            txtNombre.setText(nombres[num]);
            txtApellido.setText(apellidos[num]);
            txtDireccion.setText(direcciones[num]);
            txtTelefono.setText(telefonos[num]);
            txtNacimiento.setText(fNacimientos[num]);
        }
    }//GEN-LAST:event_btnRetrosederActionPerformed

    
    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
      String indice = txtIndice.getText();
      int indi = Integer.parseInt(indice);
        dnis[indi] = txtDni.getText();
        nombres[indi] = txtNombre.getText();
        apellidos[indi] = txtApellido.getText();
        direcciones[indi] = txtDireccion.getText();
        telefonos[indi] = txtTelefono.getText();
        fNacimientos[indi] = txtNacimiento.getText();
    }//GEN-LAST:event_btnGrabarActionPerformed

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
