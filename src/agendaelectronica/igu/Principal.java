    private void btnAdelantarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdelantarActionPerformed

        String indice = txtIndice.getText();
        int num = Integer.parseInt(indice);
        if (num < 9) {
            num = num + 1;
            indice = String.valueOf(num);
            txtIndice.setText(indice);
            
            txtDni.setText(dnis[num]);
            txtNombre.setText(nombres[num]);
            txtApellido.setText(apellidos[num]);
            txtDireccion.setText(direcciones[num]);
            txtTelefono.setText(telefonos[num]);
            txtNacimiento.setText(fNacimientos[num]);
        }
 

    }//GEN-LAST:event_btnAdelantarActionPerformed

